class Employee{

    constructor(id, Name){
        this.id = id;
        this.Name = Name;
    }



}
let emp1 = new Employee(1,'John')
let emp2 = new Employee(2,'Peter')

console.log('emp 1 id '+emp1.id)
console.log('emp 1 name '+emp1.Name)

console.log('emp 2 id '+emp2.id)
console.log('emp 2 name '+emp2.Name)

